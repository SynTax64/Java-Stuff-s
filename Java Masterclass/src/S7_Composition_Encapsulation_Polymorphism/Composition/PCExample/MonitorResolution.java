package S7_Composition_Encapsulation_Polymorphism.Composition.PCExample;

public class MonitorResolution {
    private int width;
    private int height;

    public MonitorResolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
